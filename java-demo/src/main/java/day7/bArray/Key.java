package day7.bArray;

/**
 * IDEA常见的快捷键
 * Ctrl + c: 拷贝选中的内容。如果没有选中任何内容，则默认拷贝当前行。
 * Ctrl + x: 剪切选中的内容。如果没有选中任何内容，则默认剪切当前行。
 * Ctrl + d: 复制当前行。
 * Alt + Shift + ↑/↓: 上下移动选中的行。
 * Ctrl + / : 单行注释
 * Ctrl + Shift + / : 多行注释
 * Ctrl + +/- : 展开、折叠代码段
 * Ctrl + [/] : 跳转到当前代码段的开头、结尾
 * Ctrl + p: 显式方法的参数
 * Ctrl + Alt + l: 代码格式化
 * Alt + 鼠标上下拖动: 块选
 */
public class Key {
    public static void main(String[] args) {
        System.out.println();
        int a = 10, b = 20;
        if (a > b) {
            System.out.println("aaa");
        } else {
            System.out.println("bbb");
        }
    }
}
