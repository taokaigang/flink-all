package day3.aExercise;

public class Three {
    public static void main(String[] args) {
        // 设计一段程序，实现将两个byte类型的变量相加，并将结果给一个byte变量赋值
        // 1. 定义两个byte类型的变量
        byte a = 10, b = 20;
        // 2. 相加，并给一个新的byte赋值
        byte sum = (byte)(a + b);
        // 3. 输出结果
        System.out.println(sum);
    }
}
