package day3.bOperator;

public class G {
    // 运算符的优先级

    // 运算符分类： 按照每一个运算符可以操作的数据数量，可以分为：一元运算符、二元运算符、三元运算符
    // 一元运算符：只能操作一个变量， ++  --  !   ~   +   -
    // 二元运算符：只能操作两个变量， + - * / % > < >= <= ...
    // 三元运算符：可以操作三个变量，目前，只有一个三元运算符，就是三目运算符

    // 一元运算符 > 二元运算符 > 三元运算符
    // 算术运算： 乘、除、模 > 加、减
    // 逻辑运算符： 与 > 或、异或
    //
    // 赋值运算符：优先级最低，低于三元运算符，当等号右侧所有的逻辑都执行完了，才会给左边的变量进行取值

    // 如果实在不确定运算符的优先级，使用小括号

}
